#include "../minishell.h"

void	echo(char *str)
{
	size_t	i;

	i = 0;
	if (!str)
		return (1);
	while (str[i])
		i++;
	write(1, &str, i);
	//Condition pour l'option -n
	write(1, "\n", 1);
}
